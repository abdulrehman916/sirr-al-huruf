/**
 * Rule-based Qasam resolver.
 * Selects the single Qasam rule matching the current Mizan selections.
 *
 * Selection inputs (all from Mizaan9Page selections):
 *   - selections.days    → Day key (sun/mon/tue/wed/thu/fri/sat) — PRIMARY selector
 *   - selections.planet  → Planetary Hour planet key (sems/kamer/merih/utarid/mustari/zuhre/zuhal)
 *   - selections.purposes → Ritual type key (celb/tard/sihhat/sekam/tarfet)
 *   - selections.khayrSharr8 → Khayr / Sharr selection
 *
 * Returns { entry, conditions } where:
 *   entry     — the matched Qasam rule (da3wa + qasam), or null if no day selected
 *   conditions — validation/status info derived from the rules:
 *     - daySelected:    boolean
 *     - hourMatchesDay: boolean (planet of selected hour == planet of selected day, per PDF rule)
 *     - hourPlanet:     string | null
 *     - dayPlanet:      string | null
 *     - ritualType:     string | null
 *     - khayrSharr:     string | null
 */
export function resolveQasam(selections) {
  if (!selections) return { entry: null, conditions: null };

  const dayKey = selections.days ?? selections.day ?? null;
  const hourPlanet = selections.planet ?? null;
  const ritualType = selections.purposes ?? selections.purpose ?? null;
  const khayrSharr = selections.khayrSharr8 ?? selections.khayrSharr ?? null;

  const entry = dayKey
    ? QASAM_RULES.find((r) => r.dayKey === String(dayKey)) || null
    : null;

  const dayPlanet = entry ? entry.planet : (dayKey ? DAY_PLANET[dayKey] : null);
  const hourMatchesDay = !!(dayPlanet && hourPlanet && dayPlanet === hourPlanet);

  return {
    entry,
    conditions: {
      daySelected: !!dayKey,
      hourMatchesDay,
      hourPlanet,
      dayPlanet,
      ritualType,
      khayrSharr,
    },
  };
}