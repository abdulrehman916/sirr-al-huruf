/**
 * Resolve the Qasam entry from a 'days' selection value.
 * The selection can be an Arabic day name (يوم الأحد), an English name,
 * or a numeric index (0–6, 0 = Sunday). Returns null if not found.
 */
export function getQasamForDay(daySelection) {
  if (!daySelection) return null;
  const s = String(daySelection).trim().toLowerCase();

  // Direct key match
  if (QASAM_BY_DAY[s]) return QASAM_BY_DAY[s];

  // Arabic name map
  const arabicMap = {
    "الأحد": "sunday", "احد": "sunday", "أحد": "sunday",
    "الاثنين": "monday", "الإثنين": "monday", "اثنين": "monday", "إثنين": "monday",
    "الثلاثاء": "tuesday", "ثلاثاء": "tuesday",
    "الأربعاء": "wednesday", "الاربعاء": "wednesday", "أربعاء": "wednesday", "اربعاء": "wednesday",
    "الخميس": "thursday", "خميس": "thursday",
    "الجمعة": "friday", "جمعة": "friday",
    "السبت": "saturday", "سبت": "saturday",
  };
  for (const [k, v] of Object.entries(arabicMap)) {
    if (s.includes(k)) return QASAM_BY_DAY[v];
  }

  // English name match
  const englishMap = {
    sun: "sunday", mon: "monday", tue: "tuesday", wed: "wednesday",
    thu: "thursday", fri: "friday", sat: "saturday",
  };
  for (const [k, v] of Object.entries(englishMap)) {
    if (s.includes(k)) return QASAM_BY_DAY[v];
  }

  // Numeric index (0–6, Sunday = 0)
  const idx = parseInt(s, 10);
  const indexMap = ["sunday","monday","tuesday","wednesday","thursday","friday","saturday"];
  if (!isNaN(idx) && indexMap[idx]) return QASAM_BY_DAY[indexMap[idx]];

  return null;
}